    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) AllProductTable.getModel();
        dtm.setRowCount(0);
        for (Product p : business.getHospitalitySupplierProductCatalog().getProductList()) {
            Object[] row = new Object[7];
            row[0] = p.getId();
            row[1] = p.getName();
            row[2] = p.getPrice();
            row[3] = p.getQuantity();

            dtm.addRow(row);
        }
    }

