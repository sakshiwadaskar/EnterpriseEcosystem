    private void btnAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductActionPerformed

        if (txtProductName.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Product cannot be created,"
                    + " please enter id and name!", "Info", JOptionPane.INFORMATION_MESSAGE);
        } else {
            ProductCatalog pc = business.getHospitalitySupplierProductCatalog();
            String productName = txtProductName.getText();

            float price = Float.parseFloat(txtPrice.getText());
            int quantity = Integer.parseInt(txtAvailability.getText());

            Product product = new Product(productName, price, quantity);
            product.setName(productName);
            product.setId(String.valueOf(pc.getProductList().size() + 1000));
            product.setPrice(price);
            product.setQuantity(quantity);
            product.setSalesPersonProfile(salesPersonProfile);

            pc.addProduct(product);
            product.setId(String.valueOf(pc.getProductList().size() + 1000));

            populateTable();

            JOptionPane.showMessageDialog(this, "Product added!", "Info",
                    JOptionPane.INFORMATION_MESSAGE);

        }

    }//GEN-LAST:event_btnAddProductActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed

        CardSequencePanel.remove(this);
        ((java.awt.CardLayout) CardSequencePanel.getLayout()).previous(CardSequencePanel);
        SalesRepresentativeLandingPageJPanel salesRepresentativeLandingPanel = new SalesRepresentativeLandingPageJPanel(business, salesPersonProfile, CardSequencePanel);
        CardSequencePanel.removeAll();
        CardSequencePanel.add("SalesRepresentativeLandingPageJPanel", salesRepresentativeLandingPanel);
        ((java.awt.CardLayout) CardSequencePanel.getLayout()).next(CardSequencePanel);

    }//GEN-LAST:event_btnBackActionPerformed

    private void btnDeleteProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteProductActionPerformed
        // TODO add your handling code here:

        int selectedRow = AllProductTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row first",
                    "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Product product = business.getHospitalitySupplierProductCatalog().getProductList().get(selectedRow);
            business.getHospitalitySupplierProductCatalog().deleteProduct(product);
            populateTable();
        }


    }//GEN-LAST:event_btnDeleteProductActionPerformed

    private void btnUpdateProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateProductActionPerformed

        int selectedRow = AllProductTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row first",
                    "Warning", JOptionPane.WARNING_MESSAGE);

        } else {
            btnSave.setVisible(true);
            txtProductName.setEnabled(true);

            txtPrice.setEnabled(true);
            txtAvailability.setEnabled(true);

            Product product = business.getHospitalitySupplierProductCatalog().getProductList().get(selectedRow);
            txtProductName.setText(product.getName());

            txtPrice.setText(String.valueOf(product.getPrice()));
            txtAvailability.setText(String.valueOf(product.getQuantity()));

            btnSave.setVisible(true);
            btnAddProduct.setVisible(false);

        }

    }//GEN-LAST:event_btnUpdateProductActionPerformed

